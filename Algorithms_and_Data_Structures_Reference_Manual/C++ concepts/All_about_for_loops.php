<!-- Range based for loops -->

- executes a for loop over a range.

<!-- Syntax -->

for (range_declaration : range_expression)

loop_statement

* range_declaration - a declaration of a named variable, whose type is the type of element
of the sequence represented by range_expression, or a reference to that type. 
- Often uses the "auto" specifier for automatic type deduction

* range_expression - any expression that represents a suitable sequence or a braced-init-list

* loop_statement: body of the loop

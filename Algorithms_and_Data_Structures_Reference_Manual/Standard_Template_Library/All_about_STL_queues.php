<!-- What are Queues in STL -->

<!-- Reference - https://www.geeksforgeeks.org/queue-cpp-stl/ -->

- are a type of container adaptors which operate in a first in first out(FIFO) type of arrangement
- Elements are inserted at the back(end) and are deleted from the front

The functions supported by queue are :-

1. empty()
- returns whether the queue is empty

2. size()
- returns the size of the queue

3. front()
- returns a reference to the first element

4. back()
- returns a reference to the last element

5. push(g)
- adds an element 'g' at the end of the queue

6. pop()
- deletes the first element of the que

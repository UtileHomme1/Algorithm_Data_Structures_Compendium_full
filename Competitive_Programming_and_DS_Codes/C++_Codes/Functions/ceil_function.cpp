/*
ceil(x) - returns the smallest integer that is greater than or equal to x (i.e. rounds
down to the nearest integer)
*/

#include <bits/stdc++.h>

using namespace std;

int main()
{
  cout<<"Ceil is: "<<ceil(2.3)<<endl;
  cout<<"Ceil is: "<<ceil(-2.3)<<endl;

  return 0;
}

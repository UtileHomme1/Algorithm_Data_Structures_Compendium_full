/*
floor(x) - returns the largest integer that is smaller than or equal to x (i.e. rounds
downs to the nearest integer)
*/

#include <bits/stdc++.h>

using namespace std;

int main()
{
  cout<<"Floor is: "<<floor(2.3)<<endl;
  cout<<"Floor is: "<<floor(-2.3)<<endl;

  return 0;
}
